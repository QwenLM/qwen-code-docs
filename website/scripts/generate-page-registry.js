#!/usr/bin/env node

const fs = require("fs");
const path = require("path");

const CONTENT_DIR = path.join(__dirname, "..", "content");
const OUTPUT_FILE = path.join(
  __dirname,
  "..",
  "src",
  "generated",
  "page-registry.js"
);
const PAGE_EXTENSIONS = new Set([".md", ".mdx"]);

function walk(dir) {
  const entries = fs.readdirSync(dir, { withFileTypes: true });
  const files = [];

  for (const entry of entries) {
    const fullPath = path.join(dir, entry.name);
    if (entry.isDirectory()) {
      files.push(...walk(fullPath));
    } else if (entry.isFile() && PAGE_EXTENSIONS.has(path.extname(entry.name))) {
      files.push(fullPath);
    }
  }

  return files;
}

function toPosixPath(filePath) {
  return filePath.split(path.sep).join("/");
}

function toRoute(relativePath) {
  const parsed = path.parse(relativePath);
  const withoutExtension = path.join(parsed.dir, parsed.name);

  if (parsed.name === "index") {
    return toPosixPath(parsed.dir);
  }

  return toPosixPath(withoutExtension);
}

function toImportPath(filePath) {
  const relative = path.relative(path.dirname(OUTPUT_FILE), filePath);
  const normalized = toPosixPath(relative);
  return normalized.startsWith(".") ? normalized : `./${normalized}`;
}

function generatePageRegistry() {
  const locales = fs
    .readdirSync(CONTENT_DIR, { withFileTypes: true })
    .filter((entry) => entry.isDirectory())
    .map((entry) => entry.name)
    .sort((a, b) => a.localeCompare(b));

  const pages = [];

  for (const locale of locales) {
    const localeDir = path.join(CONTENT_DIR, locale);
    for (const filePath of walk(localeDir)) {
      const relativeToLocale = path.relative(localeDir, filePath);
      const route = toRoute(relativeToLocale);
      pages.push({
        key: `${locale}/${route}`,
        locale,
        route,
        importPath: toImportPath(filePath),
      });
    }
  }

  pages.sort((a, b) => a.key.localeCompare(b.key));

  const staticParams = pages.map((page) => ({
    lang: page.locale,
    mdxPath: page.route ? page.route.split("/") : [],
  }));

  const lines = [
    "// This file is generated by scripts/generate-page-registry.js.",
    "// Do not edit it directly.",
    "",
    "const pageLoaders = {",
    ...pages.map(
      (page) =>
        `  ${JSON.stringify(page.key)}: () => import(${JSON.stringify(
          page.importPath
        )}),`
    ),
    "};",
    "",
    `export const staticParams = ${JSON.stringify(staticParams, null, 2)};`,
    "",
    "export async function importContentPage(mdxPath = [], lang = \"\") {",
    "  const segments = Array.isArray(mdxPath) ? mdxPath.filter(Boolean) : mdxPath ? [mdxPath] : [];",
    "  const route = segments.join(\"/\");",
    "  const key = `${lang}/${route}`;",
    "  const loader = pageLoaders[key];",
    "",
    "  if (!loader) {",
    "    throw new Error(`Cannot find content page for ${key}`);",
    "  }",
    "",
    "  return loader();",
    "}",
    "",
  ];

  fs.mkdirSync(path.dirname(OUTPUT_FILE), { recursive: true });
  fs.writeFileSync(OUTPUT_FILE, `${lines.join("\n")}\n`, "utf8");
  console.log(`Generated page registry: ${pages.length} pages -> ${OUTPUT_FILE}`);
}

generatePageRegistry();
